from mem_transformer import MemTransformerLM
import lamb

def transformer(batchsize, local_rank, do_eval=True, profile=None):

    model_config = {
        'n_token': 267735,
        'n_layer': 16,
        'n_head': 8,
        'd_model': 512,
        'd_head': 64,
        'd_inner': 2048,
        'dropout': 0.1,
        'dropatt': 0.0,
        'dtype': None,
        'tie_weight': True,
        'd_embed': 512,
        'div_val': 1,
        'tie_projs': [False, True, True, True],
        'pre_lnorm': False,
        'tgt_len': 192,
        'ext_len': 0,
        'mem_len': 192,
        'cutoffs': [19997, 39997, 199997],
        'same_length': False,
        'attn_type': 0,
        'clamp_len': -1,
        'sample_softmax': -1
    }

    data = torch.ones((192, batchsize)).to(torch.int64).cuda()
    target = torch.ones((192, batchsize)).to(torch.int64).cuda()
    mems = torch.ones((16, 192, batchsize, 512)).to(torch.int64).cuda()

    model = MemTransformerLM(**model_config).to(0)

    if do_eval:
        model.eval()
    else:
        model.train()
        optimizer = lamb.Lamb(model.parameters(), lr=0.1)

    torch.cuda.synchronize()
    batch_idx = 0

    while batch_idx < 100:

        if batch_idx == 0:
            if profile == 'ncu':
                torch.cuda.nvtx.range_push("start")
            elif profile == 'nsys':
                torch.cuda.profiler.cudart().cudaProfilerStart()

        if do_eval:
            with torch.no_grad():
                output = model(data, target, mems)
        else:
            loss, output = model(data, target, mems)
            loss = loss.float().mean().type_as(loss)
            loss.backward()
            optimizer.step()

        if batch_idx == 0:
            if profile == 'ncu':
                torch.cuda.nvtx.range_pop()
            elif profile == 'nsys':
                torch.cuda.profiler.cudart().cudaProfilerStop()

        batch_idx += 1

    print("Done!")

if __name__ == "__main__":
    transformer(8, 0, False, 'nsys')

